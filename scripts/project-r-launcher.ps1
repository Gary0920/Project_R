function Install-ProjectShortcuts {
    Ensure-ProjectIcon

    $desktop = [Environment]::GetFolderPath("DesktopDirectory")
    $shell = New-Object -ComObject WScript.Shell
    $powershell = (Get-Command powershell.exe).Source
    $startName = "Project_R $(Join-Chars @(0x542F, 0x52A8)).lnk"
    $statusName = "Project_R $(Join-Chars @(0x72B6, 0x6001)).lnk"
    $stopName = "Project_R $(Join-Chars @(0x505C, 0x6B62)).lnk"

    $items = @(
        [pscustomobject]@{ Name = $startName; Action = "Start"; Description = "Start Project_R" },
        [pscustomobject]@{ Name = $statusName; Action = "Status"; Description = "Show Project_R status" },
        [pscustomobject]@{ Name = $stopName; Action = "Stop"; Description = "Stop Project_R" }
    )

    foreach ($item in $items) {
        $shortcutPath = Join-Path $desktop $item.Name
        $shortcut = $shell.CreateShortcut($shortcutPath)
        $shortcut.TargetPath = $powershell
        $shortcut.Arguments = "-NoProfile -ExecutionPolicy Bypass -File `"$PSCommandPath`" -Action $($item.Action)"
        $shortcut.WorkingDirectory = $RepoRoot.Path
        $shortcut.IconLocation = "$IconPath,0"
        $shortcut.Description = $item.Description
        $shortcut.WindowStyle = if ($item.Action -eq "Start") { 7 } else { 1 }
        $shortcut.Save()
        if (-not (Test-Path -LiteralPath $shortcutPath)) {
            throw "Shortcut was not created: $shortcutPath"
        }
    }

    $message = @(
        "Desktop shortcuts installed:",
        (Join-Path $desktop $startName),
        (Join-Path $desktop $statusName),
        (Join-Path $desktop $stopName),
        "",
        "提示：启动 (Start) 现已自动开通局域网和防火墙规则。",
        '如果桌面上有旧的"内网端口"快捷方式，可以手动删除。'
    ) -join [Environment]::NewLine
    Show-LauncherMessage -Title "Project_R Shortcuts" -Message $message
}

try {
    switch ($Action) {
        "Start" {
            Start-ProjectR
        }
        "Stop" {
            Stop-ProjectR
            if ($script:LastStopWarnings.Count -gt 0) {
                $message = @(
                    "Project_R frontend/dev processes have been stopped.",
                    "",
                    "Warning:",
                    ($script:LastStopWarnings -join [Environment]::NewLine)
                ) -join [Environment]::NewLine
                Show-LauncherMessage -Title "Project_R Stop" -Message $message -Icon "Warning"
            } else {
                Show-LauncherMessage -Title "Project_R Stop" -Message "Project_R dev processes and ports have been stopped."
            }
        }
        "Status" {
            Show-ProjectStatus
        }
        "Restart" {
            Stop-ProjectR
            Start-ProjectR
        }
        "InstallShortcuts" {
            Install-ProjectShortcuts
        }
    }
} catch {
    $message = @(
        "Action failed: $Action",
        $_.Exception.Message,
        "",
        "Logs:",
        $BackendLog,
        $BackendErrorLog,
        $FrontendLog,
        $FrontendErrorLog,
        $LauncherLog
    ) -join [Environment]::NewLine
    Write-LauncherLog $message
    Show-LauncherMessage -Title "Project_R Launcher Error" -Message $message -Icon "Error"
    exit 1
}
